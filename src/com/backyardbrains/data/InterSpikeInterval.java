package com.backyardbrains.data;

public class InterSpikeInterval {

    private final float x;
    private final int y;

    public InterSpikeInterval(float x, int y) {
        this.x = x;
        this.y = y;
    }

    public float getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}